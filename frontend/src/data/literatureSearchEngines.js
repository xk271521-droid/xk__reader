function normalizeQuery(query) {
  return String(query || '')
    .trim()
    .replace(/\s+/g, ' ')
}

function encodeQuery(query) {
  return encodeURIComponent(normalizeQuery(query))
}

function createPreviewFallback(name, subtitle, color) {
  return {
    mode: 'generated',
    title: name,
    subtitle,
    color,
  }
}

