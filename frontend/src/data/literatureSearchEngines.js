export function buildLiteratureSearchUrl(engine, query) {
  const normalized = normalizeQuery(query)
  if (normalized && engine?.searchUrlTemplate) {
    return engine.searchUrlTemplate.replace('{query}', encodeQuery(normalized))
  }
  return engine?.homepageUrl || ''
}

export function canPrefillLiteratureQuery(engine) {
  return Boolean(engine?.searchUrlTemplate)
}

export function getLiteratureEngineById(engineId) {
  return literatureSearchEngines.find((engine) => engine.id === engineId) || literatureSearchEngines[0]
}

export function groupLiteratureEngines() {
  return literatureSearchEngines.reduce((groups, engine) => {
    const current = groups.get(engine.resourceType) || []
    current.push(engine)
    groups.set(engine.resourceType, current)
    return groups
  }, new Map())
}
